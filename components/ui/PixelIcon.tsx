export default function PixelIcon({ type, size = 16, color = "currentColor", className = "" }: PixelIconProps) {
  const pixels = PIXELS[type] ?? [];
  const cellSize = size / 7;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 7 7"
      style={{ imageRendering: "pixelated", shapeRendering: "crispEdges" }}
      className={className}
    >
      {pixels.map(([x, y], i) => (
        <rect key={i} x={x} y={y} width={1} height={1} fill={color} />
      ))}
    </svg>
  );
}
